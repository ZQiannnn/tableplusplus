    static let shared = L10n()

    enum Language: String, CaseIterable {
        case auto, en, zh
        var label: String {
            switch self {
            case .auto: "Auto"
            case .en:   "English"
            case .zh:   "中文"
            }
        }
    }

    var language: Language {
        get { Language(rawValue: PrefsStore.shared.language) ?? .auto }
        set { PrefsStore.shared.setLanguage(newValue.rawValue) }
    }

    private init() {}

    /// Resolved current locale ("en" or "zh") after applying auto-detect.
    var resolved: Language {
        let lang = language
        if lang == .auto {
            let code = Locale.current.language.languageCode?.identifier ?? "en"
            return code.hasPrefix("zh") ? .zh : .en
        }
        return lang
    }

    static func t(_ key: String) -> String {
        let lang = shared.resolved
        let dict: [String: String]
        switch lang {
        case .zh: dict = zh
        case .en, .auto: dict = en
        }
        return dict[key] ?? key
    }

